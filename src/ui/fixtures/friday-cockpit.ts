/**
 * Fixed reference "now" for the prototype. Friday morning, 2026-05-08 at
 * 08:30 America/Chicago (the founder's locale; OK pilots first).
 *
 * The cockpit page reads `REFERENCE_NOW` rather than `Date.now()` so screenshots
 * are stable. When wired to a real backend, swap to `Date.now()`.
 */
export const REFERENCE_NOW = new Date('2026-05-08T13:30:00Z');

/** Convenience: hours added to REFERENCE_NOW returns an ISO timestamp. */
function plus(hours: number): string {
  return new Date(REFERENCE_NOW.getTime() + hours * 60 * 60 * 1000).toISOString();
}

/** Convenience: hours before REFERENCE_NOW. */
function minus(hours: number): string {
  return new Date(REFERENCE_NOW.getTime() - hours * 60 * 60 * 1000).toISOString();
}

/** Convenience: ISO date (no time) by day offset. */
function day(offsetDays: number): string {
  const d = new Date(REFERENCE_NOW.getTime() + offsetDays * 24 * 60 * 60 * 1000);
  const iso = d.toISOString();
  return iso.slice(0, 10) ?? iso;
}

